InitShadowOAM:
	; Copy Instruction into high ram ($FF80)
	ld bc,ShadowOAMDMA_Head
	ld hl,$FF80
	ld d,(ShadowOAMDMA_Tail-ShadowOAMDMA_Head)
	
_LoadLoop:
	ld a,(bc)
	ld (hl),a
	inc bc
	inc hl
	dec d
	ld a,d
	cp $00
	jr nz,_LoadLoop
	
	; Initialize Shadow OAM to 0
	ld hl,SHADOW_OAM_HEAD
	ld de,SHADOW_OAM_TAIL
	ld b,$00
	call MemSet
	ret

	; SHADOW OAM
	org $1F80

	; Shadow OAM Code
ShadowOAMDMA_Head:
	ld a,$C1
	ld (DMA),a
	ld a,$28
_Wait:
	dec a
	jp nz,$FF86
	ret
ShadowOAMDMA_Tail:
	
ShadowOAMRam group $00
	org $C100

